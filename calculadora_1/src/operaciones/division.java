public class division {
    private int n1;
    private int n2;

    public division(int n1, int n2) {
        this.n1 = n1;
        this.n2 = n2;
    }

    public int mostrarDivision() {
        int rDivision;
        rDivision = n1 / n2;
        return rDivision;
    }
    
}
