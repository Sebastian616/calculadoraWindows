public class multiplicacion {
    
    private int n1;
    private int n2;

    public multiplicacion(int n1, int n2) {
        this.n1 = n1;
        this.n2 = n2;
    }

    public int mostrarMulti() {
        int rMulti;
        rMulti = n1 * n2 ;
        return rMulti;
    }
    
    
}
